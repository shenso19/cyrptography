// Rcon 
const word Rcon[11] = {
    0x00000000,
    0x01000000,
    0x02000000,
    0x04000000,
    0x08000000,
    0x10000000,
    0x20000000,
    0x40000000,
    0x80000000,
    0x1b000000,
    0x36000000
};

byte ffAdd(byte a, byte b) {
    return a ^ b;
}

byte xtime(byte a) {
    return (byte)((a << 1) ^ ((a & 0x80) ? 0x1b : 0x00));
}

byte ffMultiply(byte a, byte b)
{
    byte result = 0;

    while (b)
    {
        if (b & 1)
            result ^= a;

        a = xtime(a);
        b >>= 1;
    }

    return result;
}

word rotWord(word w) {
    return (w << 8) | (w >> 24);
}

word subWord(word w) {
    return (word(SBox[(w >> 24) & 0xff]) << 24) |
           (word(SBox[(w >> 16) & 0xff]) << 16) |
           (word(SBox[(w >>  8) & 0xff]) <<  8) |
           (word(SBox[(w      ) & 0xff]));
}

void keyExpansion(const byte key[], word w[], int Nk, int Nr) {
    int totalWords = Nb * (Nr + 1);

    for (int i = 0; i < Nk; i++) {
        w[i] = (word(key[4*i])   << 24) |
               (word(key[4*i+1]) << 16) |
               (word(key[4*i+2]) << 8)  |
               (word(key[4*i+3]));
    }

    for (int i = Nk; i < totalWords; i++) {
        word temp = w[i - 1];

        if (i % Nk == 0) {
            temp = subWord(rotWord(temp)) ^ Rcon[i / Nk];
        } else if (Nk > 6 && (i % Nk == 4)) {
            temp = subWord(temp);
        }

        w[i] = w[i - Nk] ^ temp;
    }
}

void subBytes(byte state[4][4]) {
    for (int r = 0; r < 4; r++)
        for (int c = 0; c < 4; c++)
            state[r][c] = SBox[state[r][c]];
}

void shiftRows(byte state[4][4]) {
    byte tmp[4][4];

    // row 0 unchanged
    for (int c = 0; c < 4; c++)
        tmp[0][c] = state[0][c];

    // row 1 shift left by 1
    tmp[1][0] = state[1][1];
    tmp[1][1] = state[1][2];
    tmp[1][2] = state[1][3];
    tmp[1][3] = state[1][0];

    // row 2 shift left by 2
    tmp[2][0] = state[2][2];
    tmp[2][1] = state[2][3];
    tmp[2][2] = state[2][0];
    tmp[2][3] = state[2][1];

    // row 3 shift left by 3
    tmp[3][0] = state[3][3];
    tmp[3][1] = state[3][0];
    tmp[3][2] = state[3][1];
    tmp[3][3] = state[3][2];

    for (int r = 0; r < 4; r++)
        for (int c = 0; c < 4; c++)
            state[r][c] = tmp[r][c];
}

void mixColumns(byte state[4][4]) {
    for (int c = 0; c < 4; c++) {
        byte s0 = state[0][c];
        byte s1 = state[1][c];
        byte s2 = state[2][c];
        byte s3 = state[3][c];

        state[0][c] = ffMultiply(0x02, s0) ^ ffMultiply(0x03, s1) ^ s2 ^ s3;
        state[1][c] = s0 ^ ffMultiply(0x02, s1) ^ ffMultiply(0x03, s2) ^ s3;
        state[2][c] = s0 ^ s1 ^ ffMultiply(0x02, s2) ^ ffMultiply(0x03, s3);
        state[3][c] = ffMultiply(0x03, s0) ^ s1 ^ s2 ^ ffMultiply(0x02, s3);
    }
}

void addRoundKey(byte state[4][4], const word w[], int round) {
    for (int c = 0; c < 4; c++) {
        word k = w[round * Nb + c];
        state[0][c] ^= (k >> 24) & 0xff;
        state[1][c] ^= (k >> 16) & 0xff;
        state[2][c] ^= (k >>  8) & 0xff;
        state[3][c] ^= (k      ) & 0xff;
    }
}

void invSubBytes(byte state[4][4]) {
    for (int r = 0; r < 4; r++)
        for (int c = 0; c < 4; c++)
            state[r][c] = InvSBox[state[r][c]];
}

void invShiftRows(byte state[4][4]) {
    byte tmp[4][4];

    // row 0 unchanged
    for (int c = 0; c < 4; c++)
        tmp[0][c] = state[0][c];

    // row 1 shift right by 1
    tmp[1][0] = state[1][3];
    tmp[1][1] = state[1][0];
    tmp[1][2] = state[1][1];
    tmp[1][3] = state[1][2];

    // row 2 shift right by 2
    tmp[2][0] = state[2][2];
    tmp[2][1] = state[2][3];
    tmp[2][2] = state[2][0];
    tmp[2][3] = state[2][1];

    // row 3 shift right by 3 (left by 1)
    tmp[3][0] = state[3][1];
    tmp[3][1] = state[3][2];
    tmp[3][2] = state[3][3];
    tmp[3][3] = state[3][0];

    for (int r = 0; r < 4; r++)
        for (int c = 0; c < 4; c++)
            state[r][c] = tmp[r][c];
}

void invMixColumns(byte state[4][4]) {
    for (int c = 0; c < 4; c++) {
        byte s0 = state[0][c];
        byte s1 = state[1][c];
        byte s2 = state[2][c];
        byte s3 = state[3][c];

        state[0][c] = ffMultiply(0x0e, s0) ^ ffMultiply(0x0b, s1) ^
                      ffMultiply(0x0d, s2) ^ ffMultiply(0x09, s3);
        state[1][c] = ffMultiply(0x09, s0) ^ ffMultiply(0x0e, s1) ^
                      ffMultiply(0x0b, s2) ^ ffMultiply(0x0d, s3);
        state[2][c] = ffMultiply(0x0d, s0) ^ ffMultiply(0x09, s1) ^
                      ffMultiply(0x0e, s2) ^ ffMultiply(0x0b, s3);
        state[3][c] = ffMultiply(0x0b, s0) ^ ffMultiply(0x0d, s1) ^
                      ffMultiply(0x09, s2) ^ ffMultiply(0x0e, s3);
    }
}

void printState(const char* label, int round, const byte state[4][4]) {
    std::printf("round[%2d].%s ", round, label);
    for (int c = 0; c < 4; c++)
        for (int r = 0; r < 4; r++)
            std::printf("%02x", state[r][c]);
    std::printf("\n");
}

void printRoundKey(const char* label, int round, const word w[]) {
    std::printf("round[%2d].%s ", round, label);
    for (int c = 0; c < 4; c++) {
        word k = w[round * Nb + c];
        std::printf("%02x%02x%02x%02x",
                    (k >> 24) & 0xff,
                    (k >> 16) & 0xff,
                    (k >>  8) & 0xff,
                    (k      ) & 0xff);
    }
    std::printf("\n");
}

void printInvRoundKey(const char* label, int round, const word w[]) {
    std::printf("round[%2d].%s ", round, label);
    for (int c = 0; c < 4; c++) {
        word k = w[c];
        std::printf("%02x%02x%02x%02x",
                    (k >> 24) & 0xff,
                    (k >> 16) & 0xff,
                    (k >>  8) & 0xff,
                    (k      ) & 0xff);
    }
    std::printf("\n");
}

void cipher(const byte in[16], byte out[16], const word w[], int Nr) {
    byte state[4][4];

    // load state column-major
    for (int c = 0; c < 4; c++)
        for (int r = 0; r < 4; r++)
            state[r][c] = in[4*c + r];

    printState("input", 0, state);
    printRoundKey("k_sch", 0, w);

    addRoundKey(state, w, 0);

    for (int round = 1; round < Nr; round++) {
        printState("start", round, state);
        subBytes(state);
        printState("s_box", round, state);
        shiftRows(state);
        printState("s_row", round, state);
        mixColumns(state);
        printState("m_col", round, state);
        addRoundKey(state, w, round);
        printRoundKey("k_sch", round, w);
    }

    printState("start", Nr, state);
    subBytes(state);
    printState("s_box", Nr, state);
    shiftRows(state);
    printState("s_row", Nr, state);
    addRoundKey(state, w, Nr);
    printRoundKey("k_sch", Nr, w);
    printState("output", Nr, state);

    for (int c = 0; c < 4; c++)
        for (int r = 0; r < 4; r++)
            out[4*c + r] = state[r][c];
}

void invCipher(const byte in[16], byte out[16], const word w[], int Nr) {
    byte state[4][4];

    for (int c = 0; c < 4; c++)
        for (int r = 0; r < 4; r++)
            state[r][c] = in[4*c + r];

    printState("iinput", 0, state);

    // Initial inverse key = last encryption key
    printInvRoundKey("ik_sch", 0, w + Nr * Nb);
    addRoundKey(state, w, Nr);

    for (int round = 1; round <= Nr; round++) {

        printState("istart", round, state);

        invShiftRows(state);
        printState("is_row", round, state);

        invSubBytes(state);
        printState("is_box", round, state);

        // Print inverse key schedule in reverse order
        printInvRoundKey("ik_sch", round, w + (Nr - round) * Nb);

        addRoundKey(state, w, Nr - round);

if (round != Nr)
    printState("ik_add", round, state);

if (round != Nr)
    invMixColumns(state);
    }

    printState("ioutput", Nr, state);

    for (int c = 0; c < 4; c++)
        for (int r = 0; r < 4; r++)
            out[4*c + r] = state[r][c];
}
